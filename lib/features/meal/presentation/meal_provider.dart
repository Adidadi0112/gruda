/// FutureProvider for adding a single meal.
@riverpod
Future<void> addMeal(
  AddMealRef ref, {
  required String recipeName,
  required DateTime mealDate,
  required String mealType,
  String? notes,
  String? prepGroupId,
}) async {
  print('[addMeal] Called for recipeName: $recipeName');
  try {
    final mealRepo = ref.watch(mealPlanRepositoryProvider);
    await mealRepo.addMeal(
      recipeName: recipeName,
      mealDate: mealDate,
      mealType: mealType,
      notes: notes,
      prepGroupId: prepGroupId,
    );

    // Refresh meals
    ref.invalidate(todayMealsProviderProvider);
    ref.invalidate(allMealsProviderProvider);

    print('[addMeal] ✅ Meal added');
  } catch (e) {
    print('[addMeal] ❌ Error: $e');
    rethrow;
  }
}

/// FutureProvider for bulk inserting a 3-day meal prep.
@riverpod
Future<String> bulkInsertMealPrep(
  BulkInsertMealPrepRef ref, {
  required List<Map<String, dynamic>> meals,
  String? autoGeneratedPrepGroupId,
}) async {
  print('[bulkInsertMealPrep] Called with ${meals.length} meals');
  try {
    final mealRepo = ref.watch(mealPlanRepositoryProvider);
    final shoppingRepo = ref.watch(shoppingRepositoryProvider);

    final prepGroupId = await mealRepo.bulkInsertMealPrep(
      meals: meals,
      autoGeneratedPrepGroupId: autoGeneratedPrepGroupId,
    );

    // Automatically generate shopping list from the meal prep
    // Use ingredientsList if available, fallback to recipe names
    final shoppingItems = <Map<String, dynamic>>[];
    final seenIngredients = <String>{};

    for (final meal in meals) {
      final ingredientsList =
          meal['ingredientsList'] as List<Map<String, dynamic>>?;
      final recipeName = meal['recipeName'] as String;

      if (ingredientsList != null && ingredientsList.isNotEmpty) {
        // Format each ingredient as "[Amount] [Unit] [Name]"
        for (final ingredient in ingredientsList) {
          final name = ingredient['name'] as String? ?? '';
          final amount = ingredient['amount'] as String?;
          final unit = ingredient['unit'] as String?;

          if (name.isNotEmpty) {
            // Build formatted ingredient string
            final parts = <String>[];
            if (amount != null && amount.isNotEmpty) parts.add(amount);
            if (unit != null && unit.isNotEmpty) parts.add(unit);
            parts.add(name);
            final formatted = parts.join(' ');

            if (!seenIngredients.contains(formatted)) {
              seenIngredients.add(formatted);
              shoppingItems.add({
                'title': formatted,
                'linkedPrepGroupId': prepGroupId,
              });
            }
          }
        }
      } else {
        // Fallback to recipe name if no ingredients
        if (!seenIngredients.contains(recipeName)) {
          seenIngredients.add(recipeName);
          shoppingItems.add({
            'title': recipeName,
            'linkedPrepGroupId': prepGroupId,
          });
        }
      }
    }

    if (shoppingItems.isNotEmpty) {
      await shoppingRepo.bulkAddShoppingItems(shoppingItems);
      print(
        '[bulkInsertMealPrep] ✅ Generated ${shoppingItems.length} shopping items',
      );
    }

    // Refresh meals and shopping list
    ref.invalidate(todayMealsProviderProvider);
    ref.invalidate(allMealsProviderProvider);
    ref.invalidate(shoppingListProviderProvider);

    print(
      '[bulkInsertMealPrep] ✅ Bulk meal prep inserted with prepGroupId: $prepGroupId',
    );
    return prepGroupId;
  } catch (e) {
    print('[bulkInsertMealPrep] ❌ Error: $e');
    rethrow;
  }
}

