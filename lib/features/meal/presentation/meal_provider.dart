/// Provider for the MealPlanRepository instance.
@riverpod
MealPlanRepository mealPlanRepository(MealPlanRepositoryRef ref) {
  final supabase = ref.watch(supabaseProvider);
  final profileAsync = ref.watch(currentProfileProvider);

  // Get household ID from profile
  String getHouseholdId() {
    if (profileAsync.hasValue) {
      return profileAsync.value?.householdId ?? '';
    }
    return '';
  }

  return MealPlanRepositoryImpl(supabase, getHouseholdId: getHouseholdId);
}

