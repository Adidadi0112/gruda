/// CombinedProvider for shopping cycles (grouped by prep_group_id).
@riverpod
Stream<List<ShoppingCycle>> shoppingCyclesProvider(
  ShoppingCyclesProviderRef ref,
) {
  print('[shoppingCyclesProvider] Fetching shopping cycles...');
  final shoppingRepo = ref.watch(shoppingRepositoryProvider);
  final mealRepo = ref.watch(mealPlanRepositoryProvider);

  // Combine both streams
  return shoppingRepo.watchShoppingItems().asyncExpand((shoppingItems) {
    return mealRepo.watchAllMeals().map((allMeals) {
      print(
        '[shoppingCyclesProvider] Processing ${shoppingItems.length} items and ${allMeals.length} meals',
      );

      // Group shopping items by prepGroupId
      final cycles = <String?, List<ShoppingItem>>{};

      for (final item in shoppingItems) {
        final groupId = item.linkedPrepGroupId ?? 'manual';
        cycles.putIfAbsent(groupId, () => []).add(item);
      }

      // Convert groups to ShoppingCycle objects with date ranges
      final shoppingCycles = <ShoppingCycle>[];

      for (final entry in cycles.entries) {
        final groupId = entry.key;
        final items = entry.value;

        if (groupId == 'manual') {
          // Manual items - no associated prep group
          shoppingCycles.add(
            ShoppingCycle(
              prepGroupId: null,
              startDate: DateTime.now(),
              endDate: DateTime.now(),
              mealPrepSetName: 'Ręcznie dodane',
              items: items,
            ),
          );
        } else {
          // Find associated meals to get the date range
          final associatedMeals = allMeals
              .where((meal) => meal.prepGroupId == groupId)
              .toList();

          if (associatedMeals.isNotEmpty) {
            associatedMeals.sort((a, b) => a.mealDate.compareTo(b.mealDate));
            final startDate = associatedMeals.first.mealDate;
            final endDate = associatedMeals.last.mealDate;

            shoppingCycles.add(
              ShoppingCycle(
                prepGroupId: groupId,
                startDate: startDate,
                endDate: endDate,
                items: items,
              ),
            );
          }
        }
      }

      // Sort cycles by start date
      shoppingCycles.sort((a, b) => a.startDate.compareTo(b.startDate));

      print(
        '[shoppingCyclesProvider] ✅ Created ${shoppingCycles.length} cycles',
      );
      return shoppingCycles;
    });
  });
}

/// StateProvider to track the selected shopping cycle index.
@riverpod
class SelectedCycleIndex extends _$SelectedCycleIndex {
  @override
  int build() => 0;

  void select(int index) => state = index;

  void next() => state++;

  void previous() {
    if (state > 0) state--;
  }
}

